static aui_hdl deca_hdl = NULL;
static aui_hdl snd_hdl = NULL;
static unsigned int g_beep_start_flag = 0;

unsigned long test_deca_beep(unsigned long *argc,char **argv,char *sz_out_put)
{
    unsigned char *p_beep_file_buf = NULL;
    unsigned int beep_file_size = 0;
    char file_path[255] = {0};
	FILE *fp = NULL;
    unsigned int time_interval = 0;
    aui_deca_stream_play_param param;
    memset(&param, 0, sizeof(param));
    
    /* 1 -- use beep file */
    if (3 == *argc) {
        strcpy(file_path, argv[0]);
        AUI_PRINTF("file_path = %s\n", file_path);
        fp = fopen(file_path, "rb");
        fseek(fp, 0L, SEEK_END);
        beep_file_size = ftell(fp);
        fseek(fp, 0L, SEEK_SET);
        AUI_PRINTF("fp = %p, beep_file_size = %d\n", fp, beep_file_size);
        /* malloc a memory to store beep stream file */
        p_beep_file_buf = (unsigned char*)malloc(beep_file_size);
        if (NULL == p_beep_file_buf) {
            AUI_PRINTF("%s %d: malloc p_beep_file_buf fail!!!\n", __FUNCTION__, __LINE__);
            return AUI_RTN_FAIL;
        }
        if (beep_file_size != fread(p_beep_file_buf, 1, beep_file_size, fp)){
            AUI_PRINTF("%s %d: fread fail!!!\n", __FUNCTION__, __LINE__);
            return AUI_RTN_FAIL;
        }
        fclose(fp);
        time_interval = ATOI(argv[1]);
        param.loop_cnt = ATOI(argv[2]);
        param.puc_src = p_beep_file_buf;
        param.len = beep_file_size;
        g_beep_start_flag = 1;
    }
    /* 2 -- use beep_bin_array */
    else if (2 == *argc){
        time_interval = ATOI(argv[0]);
        param.loop_cnt = ATOI(argv[1]);
        param.puc_src = beep_bin_array;
        param.len = beep_bin_array_size;
        g_beep_start_flag = 1;
    }
    /* 3 -- beep stop */
    else if ((1 == *argc) && (0 == ATOI(argv[0]))){
        if (1 == g_beep_start_flag) {
            g_beep_start_flag = 0;
            AUI_TEST_CHECK_NULL(deca_hdl);
            /* beep stop */
            AUI_TEST_CHECK_RTN(aui_deca_str_stop(deca_hdl));
            /* deca close */
            AUI_TEST_CHECK_RTN(aui_deca_close(deca_hdl));
            deca_hdl = NULL;
            /* snd close */
            AUI_TEST_CHECK_NULL(snd_hdl);
            AUI_TEST_CHECK_RTN(aui_snd_close(snd_hdl));
            snd_hdl = NULL;
            return AUI_RTN_SUCCESS;
        } else {
            AUI_PRINTF("Please do beep start test first.\n");
            AUI_PRINTF("1. Beep file specified by yourself.\n");
            AUI_PRINTF("[Test_Items] [file_path],[time_interval],[loop_cnt]\n");
            AUI_PRINTF("             [file_path]: the path of files must be readable.\n");
            AUI_PRINTF("             [time_interval]: the unit is a millisecond.\n");
            AUI_PRINTF("beep /mnt/usb/sda1/beep.mp2,1000,5\n\n");

            AUI_PRINTF("2. Using the defined beep array.\n");
            AUI_PRINTF("[Test_Items] [time_interval],[loop_cnt]\n");
            AUI_PRINTF("             [time_interval]: the unit is a millisecond.\n");
            AUI_PRINTF("beep 1000,5\n\n");
            return AUI_RTN_FAIL;
        }
    } else {
        AUI_PRINTF("Notice: If the beep is playing, please do beep stop first.\n");
        AUI_PRINTF("        Otherwise, you will get an error return value.\n");
        AUI_PRINTF("1. Beep file specified by yourself.\n");
        AUI_PRINTF("[Test_Items] [file_path],[time_interval],[loop_cnt]\n");
        AUI_PRINTF("             [file_path]: the path of files must be readable.\n");
        AUI_PRINTF("             [time_interval]: the unit is a millisecond.\n");
        AUI_PRINTF("beep /mnt/usb/sda1/beep.mp2,1000,5\n\n");

        AUI_PRINTF("2. Using the defined beep array.\n");
        AUI_PRINTF("[Test_Items] [time_interval],[loop_cnt]\n");
        AUI_PRINTF("             [time_interval]: the unit is a millisecond.\n");
        AUI_PRINTF("beep 1000,5\n\n");

        AUI_PRINTF("3. Beep stop.\n");
        AUI_PRINTF("[Test_Items] [beep_stop]\n");
        AUI_PRINTF("beep 0\n\n");
        return AUI_RTN_FAIL;
    }

    /* snd open */
    aui_attr_snd attr_snd;
    memset(&attr_snd, 0, sizeof(attr_snd));
    attr_snd.uc_dev_idx = 0;
    if (aui_find_dev_by_idx(AUI_MODULE_SND, 0, &snd_hdl)) {
        AUI_TEST_CHECK_RTN(aui_snd_open(&attr_snd, &snd_hdl));
	}

    /* set snd volume */
    AUI_TEST_CHECK_NULL(snd_hdl);
    unsigned char mute = 0;  /* unmute */
    unsigned char volume = 50;
    AUI_TEST_CHECK_RTN(aui_snd_mute_set(snd_hdl, mute));
    if (0 == mute) {
        AUI_TEST_CHECK_RTN(aui_snd_vol_set(snd_hdl, volume));
    }

    /* deca open */
    aui_attr_deca attr_deca;
    memset(&attr_deca, 0, sizeof(aui_attr_deca));
    attr_deca.uc_dev_idx = 0;
    if (aui_find_dev_by_idx(AUI_MODULE_DECA, attr_deca.uc_dev_idx, &deca_hdl)) {
        AUI_TEST_CHECK_RTN(aui_deca_open(&attr_deca, &deca_hdl));
    }

    /* beep start */
    AUI_TEST_CHECK_NULL(deca_hdl);
    /* 
     * Not Need to call aui_deca_init_ase, because the function only create a task, 
     * So we have been called it in the aui_deca_str_play.
     * But if you still call aui_deca_init_ase, you will have a warning, and it still work.
     */
    //AUI_TEST_CHECK_RTN(aui_deca_init_ase(deca_hdl));
    AUI_TEST_CHECK_RTN(aui_deca_str_play(deca_hdl, (unsigned int)(&param)));
    AUI_TEST_CHECK_RTN(aui_deca_set_beep_interval(deca_hdl, time_interval));

    if (3 == *argc){
        /* free beep file buffer */
        if (p_beep_file_buf) {
            free(p_beep_file_buf);
            p_beep_file_buf = NULL;
        }
    }
	return AUI_RTN_SUCCESS;
}



